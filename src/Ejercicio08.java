/*
    Primer bucle.

    Hay bubles en los que sabemos el número de ejecuciones, por ejemplo las tablas de multiplicar tienes 10 ejecuciones.

    Hay otros bucles que no sabemos cuando van a acabar.

    Las ejecuciones se les llama iteraciones
 */

public class Ejercicio08 {

    public static void main(String[] args) {

        for (int i = 0 ; i <= 10; i++) {

            System.out.println("Número de iteración: " + i);

        }
    }
}
