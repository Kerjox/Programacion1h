package Comprobaciones_if;/*
    Decir sí un número es mayor que el otro y mostrarlo por pantalla
 */

public class Ejercicio02 {

    public static void main(String[] args) {

        int x, y;

        x = 56;
        y = 45;

        if (x > y) {

            System.out.println(x + " en mayor que " + y);

        }else {

            System.out.println(x + " en menor que " + y);

        }
    }
}
