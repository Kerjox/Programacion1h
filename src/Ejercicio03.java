/*
    Ifs anidados
 */
public class Ejercicio03 {
    public static void main(String[] args) {
        int x,y;
        x = 10;
        y = 9;
        if (x == y) {
            System.out.println(x + " es igual que " + y);
        } else if (x > y) {
            System.out.println(x + " en mayor que " + y);
        } else {
            System.out.println(x + " en menor que " + y);
        }

    }
}
